package if_else_java;

public class Main {

    public static void main(String[] args){

        int x = 28;

        if (x > 10 && x <= 20) { // 11 - 20
            System.out.println("Hello java");
        }else{
            System.out.println("Bye java");
        }

        int a = 5;
        int b = 7;

        if (a < b) {
            System.out.println(a);
        }else{
            System.out.println(b);
        }
    }
}
